/*
  FIX_MPY() - fixed-point multiplication & scaling.
  Substitute inline assembly for hardware-specific
  optimization suited to a particluar DSP processor.
  Scaling ensures that result remains 16-bit.
*/
short FIX_MPY(int16_t a, int16_t b)
{
  /* shift right one less bit (i.e. 15-1) */
  int16_t c = (a * b) >> 14;
  /* last bit shifted out = rounding-bit */
  b = c & 0x01;
  /* last shift + rounding bit */
  a = (c >> 1) + b;
  return a;
}

/*
  fix_fft() - perform forward/inverse fast Fourier transform.
  fr[n],fi[n] are real and imaginary arrays, both INPUT AND
  RESULT (in-place FFT), with 0 <= n < 2**m;
*/

int fix_fft(iq_pair_raw *f, short m)
{
  int16_t mr, nn, z, j, l, k, zstep, n, scale, qr, qi, tr, ti, wr, wi;

  n = 1 << m;

  /* max FFT size = N_WAVE */
  if (n > N_WAVE)
    return -1;

  mr = 0;
  nn = n - 1;
  scale = 0;

  /* decimation in time - re-order data */
  for (m = 1; m <= nn; ++m)
  {
    l = n;
    do
    {
      l >>= 1;
    } while (mr + l > nn);
    mr = (mr & (l - 1)) + l;

    if (mr <= m)
      continue;
    tr = f[m].i;
    f[m].i = f[mr].i;
    f[mr].i = tr;

    ti = f[m].q;
    f[m].q = f[mr].q;
    f[mr].q = ti;
  }

  l = 1;
  k = LOG2_N_WAVE - 1;
  while (l < n)
  {
    zstep = l << 1;
    for (m = 0; m < l; ++m)
    {
      j = m << k;
      /* 0 <= j < N_WAVE/2 */
      wr = Sinewave[j + N_WAVE / 4] >> 1;
      wi = -Sinewave[j] >> 1;

      for (z = m; z < n; z += zstep)
      {
        j = z + l;
        tr = FIX_MPY(wr, f[j].i) - FIX_MPY(wi, f[j].q);
        ti = FIX_MPY(wr, f[j].q) + FIX_MPY(wi, f[j].i);
        qr = f[z].i;
        qi = f[z].q;

        qr >>= 1;
        qi >>= 1;

        f[j].i = qr - tr;
        f[j].q = qi - ti;
        f[z].i = qr + tr;
        f[z].q = qi + ti;
      }
    }
    --k;
    l = zstep;
  }
  return scale;
}

int task_entry(void)
{
  uint32_t *param_list = (uint32_t *)rtos_GetParameters();
  uint32_t averages = param_list[0];
  uint32_t size = param_list[1];
  uint8_t recmodule = param_list[2];

  rec_set_recording_duration(recmodule, size);

  /*locate a memory space for results averaging for each point of each delay registers*/
  int32_t *sum_dataI = rtos_GetDataBox(size * sizeof(int32_t));
  int32_t *sum_dataQ = rtos_GetDataBox(size * sizeof(int32_t));

  /*locate mem to IQ points for data averaging*/
  struct iq_pair_raw *data_iq_raw = rtos_GetDataBox(2 * size * sizeof(int16_t));

  int i, n;
  for (n = 0; n < size; n++)
  {
    sum_dataI[n] = 0;
    sum_dataQ[n] = 0;
  }

  seq_wait_while_busy(); //wait for previous task to finish

  for (i = 0; i < averages; i++)
  {
    seq_start_at(0);

    seq_wait_while_busy();
    rec_wait_while_busy(recmodule);

    rec_get_raw_timetrace(recmodule, data_iq_raw, size);

    fix_fft(data_iq_raw, log2(size));

    for (n = 0; n < size; n++)
    {
      sum_dataI[n] += (int16_t)(data_iq_raw[n].i);
      sum_dataQ[n] += (int16_t)(data_iq_raw[n].q);
    }
    rtos_SetProgress(i * size);
  }

  rtos_FinishDataBox(sum_dataI);
  rtos_FinishDataBox(sum_dataQ);

  return 42;
}
